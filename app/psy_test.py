QUESTIONS = [
    "За последние 2 недели как часто вас беспокоило чувство тревоги или напряжения?",
    "Как часто вы не могли остановить или контролировать беспокойство?",
    "Как часто вы чувствовали подавленность или безнадёжность?",
    "Как часто вы теряли интерес или удовольствие от привычных дел?",
]

def get_result(score):
    if score <= 2:
        return (
            "По результатам теста выраженных симптомов не обнаружено.\n\n"
            "Если вы всё же чувствуете дискомфорт — консультация может быть полезна."
        )
    elif score <= 5:
        return (
            "Результаты указывают на лёгкие симптомы.\n\n"
            "Разговор со специалистом может помочь снизить напряжение."
        )
    elif score <= 8:
        return (
            "Результаты указывают на умеренно выраженные симптомы.\n\n"
            "Рекомендуется обсудить это со специалистом."
        )
    else:
        return (
            "Результаты показывают выраженные симптомы.\n\n"
            "Рекомендуется обратиться к специалисту за поддержкой."
        )
